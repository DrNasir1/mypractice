#include <stdio.h>
int main()
{
	int x, y, z, a, b, c, d, f, g;
	x=0xFFFF;
	y=0x8888;
	z=0xFFFF^0x8888;
	a=0x4567;
	b=0xABCD;
	c=0xABCD & 0x4567;
	d=0x1234;
	f=0xDCBA;
	g=0xDCBA | 0x1234;
	printf("The result of 0xFFFF^0x8888 is:%x.\n", 0xFFFF^0x8888);
	printf("The result of 0xABCD & 0x4567 is:%x.\n", 0xABCD & 0x4567);
	printf("The result of 0xDCBA | 0x1234. is:%X.\n", 0xDCBA | 0x1234);
	return 0;
}
